//define these fns add(), subtract(), multiply(), and divide()

function add(a , b){
    return a + b;
}

function subtract(a, b){
    return a -b;
}
function multiply(a, b){
    return b * a;
}
function divide(a , b){
    return a / b
}
function increment(a){
    return ++a
}
function decrement(b){
    return --b
}

function makeInt(n){
    return parseInt(n, 10)

}
function preserveDecimal(n){
    return parseFloat(n)
}
